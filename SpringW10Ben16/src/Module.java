
public class Module {
	//declare variable
	private String name;
	private float marks;
	
	//constructor
	public Module() {
		this.name = "";
		this.marks = 0.0f;
	}
	
	//setters
	public void setName(String name) {
		this.name = name;
	}
	public void setMarks(float marks) {
		this.marks = marks;
	}
	
}
